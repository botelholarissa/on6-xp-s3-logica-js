//a diferença de return vs console.log
function falaOla(){
    return "Olá"
}

console.log(falaOla())

function resultado2Mais2(){
    return 2+2
}

console.log(resultado2Mais2())
console.log(resultado2Mais2() > 5)
console.log(resultado2Mais2() * 20)

